package first_steps;

public class Strings {
	public static void main(String[] args) {
		char letter = 'a'; // stores only one character, declared with single quotes
		System.out.println(letter);
		
		char value = 66;
		System.out.println(value);
		
		value = (char) (value + 1);
		System.out.println(value);
		
		String word = "alura online tech courses";
		System.out.println(word);
		
	}
}
